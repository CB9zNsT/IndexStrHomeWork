Stroka = "это моя строка"
NewStroka = "это новая строка"

print(Stroka[0]) #вывод первого символа

print(Stroka[-1]) #вывод последнего символа через отрицательный индекс

print(Stroka[2:5]) #вывод строки с 3 по 5 символ (включительно)

print(Stroka[::-1]) #вывод строки в обратном порядке

print(len(Stroka)) #вывод длины строки

print(NewStroka + " " + Stroka) #вывод через создание новой переменной
print("это новая строка " + Stroka) #вывод без создания новой переменной